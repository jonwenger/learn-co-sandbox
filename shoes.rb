#shoe size checker
# if shoe size is less than 7 => :(
# if shoe size is between 7 && 9.5 => "Yay! New Shoes"
# if shoe size is over 9.5 => "booooo"

shoe_size = 12

if shoe_size < 7
  puts ":("

elsif shoe_size >= 7 && shoe_size <= 9.5
  puts "Yay! New shoes!"

elsif shoe_size >= 9.5 && shoe_size <= 10.5
  puts "Yay! there's 1 shoe"

else 
  puts "booooo"
end 




  
  
